public class Main {
    public static void main(String[] args) {
//        Максимальный числа для следующих типов
        System.out.println("max for byte: " + Byte.MAX_VALUE);
        System.out.println("max for short: " + Short.MAX_VALUE);
        System.out.println("max for int: " + Integer.MAX_VALUE);
        System.out.println("max for long: " + Long.MAX_VALUE);

        System.out.println("max for float: " + Float.MAX_VALUE);
        System.out.println("max for double: " + Double.MAX_VALUE);

//        Минимальные числа для следующих типов
        System.out.println("min for Byte: " + Byte.MIN_VALUE);
        System.out.println("min for short: " + Short.MIN_VALUE);
        System.out.println("min for int: " + Integer.MIN_VALUE);
        System.out.println("min for long: " + Long.MIN_VALUE);

        System.out.println("min for float: " + Float.MIN_VALUE);
        System.out.println("min for double: " + Double.MIN_VALUE);
    }
}
